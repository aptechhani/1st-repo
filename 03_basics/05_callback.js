// Function to start cooking
function start(cooking) {
    console.log("Cooking " + cooking);  // Cooking ke saath cooking ka text print hoga
  }
  
  // Function to finish cooking
  function finish(cooking) {
    console.log("Cooking " + cooking);  // Finish ke saath cooking ka text print hoga
  }
  
  // Calling start function
  start("started");  // Output: "Cooking started"
  
  // Calling finish function
  finish("finished");  // Output: "Cooking finished"
  